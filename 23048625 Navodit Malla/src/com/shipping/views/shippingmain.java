    private void addshippingdata(Shipping ship) {
        shiplist.add(ship);
        //Table add
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblship.getModel();
        defaultTableModel.addRow(new Object[]{
        ship.getShipno(),ship.getFrom(), ship.getTo(), ship.getPickup(),ship.getDrop(), ship.getNumber()
        });
    }
    private Boolean deleteship(int number) {
    for (int i = 0; i < shiplist.size(); i++) {
        if (shiplist.get(i).getShipno() == number) {
            shiplist.remove(i); // Remove the ship at the current index
            return true; // Ship found and deleted
        }
    }
    return false; // Ship not found
}
    
    public void loadshiptable(){
     DefaultTableModel model = (DefaultTableModel) tblship.getModel();
    model.setRowCount(0);
    for (Shipping ship:shiplist){
        model.addRow(new Object[]{ship.getShipno(), ship.getFrom() ,  ship.getTo(), ship.getPickup(),ship.getDrop() ,ship.getNumber()});
        
    }};
    private Boolean updateship(Shipping ship){
        int shipnumber = ship.getShipno();
        for (Shipping shipupdate:shiplist){
          if(shipnumber==shipupdate.getShipno()){
             shipupdate.setFrom(ship.getFrom());
             shipupdate.setTo(ship.getTo());
             shipupdate.setPickup(ship.getPickup());
             shipupdate.setDrop(ship.getDrop());
             shipupdate.setNumber(ship.getNumber());
              return true;
              }
        }
     return false;
     }
            private Shipping binarySearch(LinkedList<Shipping> list, int target) {
            int low = 0;
            int high = list.size() - 1;

            while (low <= high) {
                int mid = low + (high - low) / 2;
                Shipping midShipping = list.get(mid); // Access mid element

                if (midShipping.getShipno() == target) {
                    return midShipping; // Found the target
                } else if (midShipping.getShipno() < target) {
                    low = mid + 1; // Search in the right half
                } else {
                    high = mid - 1; // Search in the left half
                }
            }
            return null; // Target not found
        }


