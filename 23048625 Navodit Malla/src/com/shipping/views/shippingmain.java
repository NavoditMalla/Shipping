    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed

    try {
        // Validate Ship Number
        if (shipnotxtfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ship number cannot be empty!", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int shipno = Integer.parseInt(shipnotxtfield.getText());

        // Validate From and To
        String from = (String) fromoption.getSelectedItem();
        String to = (String) tooption.getSelectedItem();
        if (from == null || from.isEmpty() || to == null || to.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Both 'From' and 'To' locations must be selected!", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validate Pickup and Drop Locations
        String pickup = pickuptxtfield.getText();
        String drop = droptxtfield.getText();
        if (pickup.isEmpty() || drop.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pickup and Drop locations cannot be empty!", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validate Contact Number
        String number = numbertxtfield.getText();
        if (number.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Contact number cannot be empty!", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!number.matches("\\d+")) { // Ensures the number contains only digits
            JOptionPane.showMessageDialog(this, "Contact number must contain only digits!", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Create and Add Shipping Data
        Shipping shipping = new Shipping(shipno, from, to, drop, pickup, number);
        addshippingdata(shipping);

        // Show Success Message
        JOptionPane.showMessageDialog(this, "Shipping data added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
    } catch (NumberFormatException e) {
        // Handles invalid Ship Number input
        JOptionPane.showMessageDialog(this, "Ship number must be a valid integer!", "Input Error", JOptionPane.ERROR_MESSAGE);
    } catch (IllegalArgumentException e) {
        // Handles any validation errors
        JOptionPane.showMessageDialog(this, e.getMessage(), "Validation Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        // Catch any other unexpected errors
        JOptionPane.showMessageDialog(this, "An unexpected error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_createActionPerformed

